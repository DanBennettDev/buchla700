#define	SP_P1	0xA0		/* normal +1 */
#define	SP_BP1	0xA2		/* boldface +1 */

#define	SP_M1	0xA1		/* normal -1 */
#define	SP_BM1	0xA3		/* boldface -1 */

#define	SP_MN	0x1A		/* boldface - */
#define	SP_PL	0x1B		/* boldface + */

#define	D_FLAT		0xA9	/* normal flat sign */
#define	BF_FLAT		0xA9	/* boldface flat sign */

#define	D_SHARP		0xA8	/* normal sharp sign */
#define	BF_SHARP	0xA8	/* boldface sharp sign */

#define	SP_BFA	'A'		/* boldface A */
#define	SP_BFC	'C'		/* boldface C */

#define	SP_BF0	0x10		/* boldface 0 */

#define	SP_1P	0xA4		/* normal 1. */
#define	SP_1PB	0xA6		/* boldface 1. */

#define	SP_UPA	0xAB		/* up arrow */
#define	SP_DNA	0xAA		/* down arrow */
